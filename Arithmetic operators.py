from __future__ import division


Num1 = int(input("Enter any number: "))
Num2 = int(input("Enter any number: "))

# Addition 
Addition = Num1 + Num2
print(Addition)

# Subtraction 
Subtraction = Num1 - Num2
print(Subtraction)

# Multipliction 
Multiplication = Num1 * Num2
print(Multiplication)

# Division 
Division = Num1 / Num2
print(Division)

# Floor Division
Flo = Num1 // Num2
print(Flo)

# Modulus Operator 
Mod = Num1 % Num2
print(Mod)

# Exponent
Exp = Num1 ** Num2
print(Exp)




